    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int klaava = 0;
        
        ImageIcon kuvaVar = new ImageIcon("src/kuvia/7.jpg"); // Muuttuu kuva Ikoniksi väliaikaisesti
        Image kuvaksi = kuvaVar.getImage();            // Kuvaksi muuttaminen, koska haluan säätää kuvan korkeutta ja leveyttä.
        Image uusiKuva = kuvaksi.getScaledInstance(125, 125, java.awt.Image.SCALE_FAST); // Kuvaksi muuttaminen, koska haluan säätää kuvan korkeutta ja leveyttä.
        ImageIcon kuva = new ImageIcon(uusiKuva); //
        
        jLabel14.setIcon(kuva);
        
        if (!(tuplaArvo <= 0)) {
            int muuttuja = peli.tuplaus();
            if (muuttuja == klaava) {
                tuplaArvo = tuplaArvo * 2;
                bool(true);
                System.out.println("Oikein meni");
                jLabel14.setText("Kruuna tuli tuplasit voittosi");
            } else if (!(muuttuja == klaava)) {
                peli.setSaldo(peli.getSaldo() - ogVoitto);
                tuplaArvo = 0;
                bool(false);
                System.out.println("Väärin meni");
            }
        } else {
            jLabel14.setText("Ei ole mahdollista tuplata!");
            bool(false);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


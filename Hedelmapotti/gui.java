    private static ImageIcon kuva(int kuvaNumero) {
        String tekstiNumero = Integer.toString(rulla.get(kuvaNumero)); // Vaihtuva numero kuvan etsimiseen
        /**
         * Rullaan tulee 3 numero esim. 1,5,3 eli tahti,mansikka,appelsiini,
         * kuvanumero hakee rullan 0,1,2 paikat jne...
         */
        String kuvaPaikka = "src/kuvia/" + tekstiNumero + ".jpg"; // Niinkuin huomaa tekstinumero tulee vaihtamaan kuvan lähdettä
        ImageIcon kuvaVar = new ImageIcon(kuvaPaikka); // Muuttuu kuva Ikoniksi väliaikaisesti
        Image kuvaksi = kuvaVar.getImage();            // Kuvaksi muuttaminen, koska haluan säätää kuvan korkeutta ja leveyttä.
        Image uusiKuva = kuvaksi.getScaledInstance(125, 125, java.awt.Image.SCALE_FAST); // Kuvaksi muuttaminen, koska haluan säätää kuvan korkeutta ja leveyttä.
        ImageIcon kuva = new ImageIcon(uusiKuva); // Takaisin kuvaIkoniksi että sen saa Jlabeliin kiinitettyä!

        return kuva;
    }

